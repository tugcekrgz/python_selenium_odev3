# HTML NEDİR?

# HTML(Hyper Text Markup Language)=  Web sayfalarını oluşturmak için kullanılan standart metin işaretleme dilidir.
#                                    HTML,bir programlama dili olarak tanımlanamaz, kodlarıyla kendi başına çalışan 
#                                    bir program yazılamaz.Ancak bu dili yorumlayabilen programlar aracılığıyla 
#                                    çalışabilen programlar yazılabilir.web tasarımcılarına sayfalar ve uygulamalar
#                                    için profilleri ,bağlantılar, blok alıntılar,paragraflar ve başlıklar oluştur-
#                                    malarında yardımcıdır.



#   EN ÇOK KULLANILAN HTML KODLARI ;

# <!DOCTYPE html> : Tüm HTML belgeleri bu kod ile başlamak zorundadır.Bu kod sayfanın HTML5 formatına göre kodlandığını gösterir.
 
# <html> : Kök etiket olarakta bilinen bu temel kullanımda tüm HTML kodları,bu iki etiket arasıda tanımlanmak zorundadır.

# <body> : Görünebilir tüm HTML kod parçalarının eklendiği etiket alanıdır.

# <head> : Web sayfasının dili, başlığı ve diğer düzenlemelerinin yer aldığı etikettir.

# <title> : Web sitesinin başlık alanını tanımlamak için kullanılır.<title> etiketi,<head> etiketleri arasında olmak zorundadır.




